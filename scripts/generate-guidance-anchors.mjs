#!/usr/bin/env node
/**
 * Extracts the guidance anchor ids from the frontend catalog
 * (src/features/plugins/companion/guidance/anchorCatalog.ts) and generates the
 * Rust allow-list (src-tauri/src/companion/generated_anchors.rs) that the
 * dispatcher validates `point_at` / `compose_walkthrough` anchors against.
 *
 * The TS catalog is the single source of truth; this codegen keeps the Rust
 * mirror in lock-step so the two can never drift (the manual sync was the
 * bottleneck on growing Athena's "show me how" coverage).
 *
 * Usage: node scripts/generate-guidance-anchors.mjs
 */
import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const CATALOG = resolve(ROOT, "src/features/plugins/companion/guidance/anchorCatalog.ts");
const OUTPUT = resolve(ROOT, "src-tauri/src/companion/generated_anchors.rs");

const src = readFileSync(CATALOG, "utf-8");

// Isolate the ANCHOR_CATALOG object literal.
const block = src.match(/ANCHOR_CATALOG[^=]*=\s*\{([\s\S]*?)\n\};/);
if (!block) {
  console.error("Could not find `ANCHOR_CATALOG = { … };` in anchorCatalog.ts");
  process.exit(1);
}

// Each entry is `  <key>: { … },` — capture the top-level keys, skipping
// comments. The single-line value objects never start a line with `key: {`
// for their inner fields, so this only matches catalog entries.
const keys = [];
for (const line of block[1].split("\n")) {
  const t = line.trim();
  if (t.startsWith("//")) continue;
  const m = t.match(/^(\w+):\s*\{/);
  if (m) keys.push(m[1]);
}

if (keys.length === 0) {
  console.error("Parsed 0 anchors from anchorCatalog.ts — refusing to write an empty allow-list");
  process.exit(1);
}

const unique = [...new Set(keys)];

const output = `// AUTO-GENERATED by scripts/generate-guidance-anchors.mjs — DO NOT EDIT
// Re-run: node scripts/generate-guidance-anchors.mjs
//
// Generated from src/features/plugins/companion/guidance/anchorCatalog.ts (${unique.length} anchors)

/// Allow-list of guidance anchor ids Athena may target via \`point_at\` /
/// \`compose_walkthrough\`. The frontend catalog (anchorCatalog.ts) is the source
/// of truth; this mirror is regenerated by the codegen so the two never drift.
pub const GUIDANCE_ANCHORS: &[&str] = &[
${unique.map((k) => `    "${k}",`).join("\n")}
];
`;

writeFileSync(OUTPUT, output, "utf-8");
console.log(`Generated ${OUTPUT} with ${unique.length} guidance anchors.`);
