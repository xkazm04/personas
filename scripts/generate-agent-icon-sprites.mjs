import fs from "node:fs/promises";
import path from "node:path";
import sharp from "sharp";

const root = process.cwd();
const iconDir = path.join(root, "public", "agent_icons");
const outDir = iconDir;
const manifestPath = path.join(root, "src", "lib", "icons", "agentIconSprite.generated.ts");
const modes = ["dark", "light"];

const files = await fs.readdir(iconDir);
const ids = files
  .filter((name) => name.endsWith("-dark.webp"))
  // Exclude the script's own output sprite-dark.webp (and any future
  // -dark.webp output we add). Without this filter the sprite gets
  // re-fed as a source on the next run and trips the 512x512 check.
  .filter((name) => !name.startsWith("sprite-"))
  .map((name) => name.replace(/-dark\.webp$/, ""))
  .sort();

if (ids.length === 0) {
  throw new Error("No agent icon WebP files found");
}

const manifest = {};

for (const mode of modes) {
  const inputs = [];
  for (const [index, id] of ids.entries()) {
    const file = path.join(iconDir, `${id}-${mode}.webp`);
    const meta = await sharp(file).metadata();
    if (meta.width !== 512 || meta.height !== 512) {
      throw new Error(`${path.basename(file)} must be 512x512, got ${meta.width}x${meta.height}`);
    }
    inputs.push({ input: file, left: index * 512, top: 0 });
  }

  const outputName = `sprite-${mode}.webp`;
  await sharp({
    create: {
      width: ids.length * 512,
      height: 512,
      channels: 4,
      background: { r: 0, g: 0, b: 0, alpha: 0 },
    },
  })
    .composite(inputs)
    .webp({ quality: 88, effort: 5 })
    .toFile(path.join(outDir, outputName));

  manifest[mode] = {
    src: `/agent_icons/${outputName}`,
    columns: ids.length,
    cells: Object.fromEntries(ids.map((id, index) => [id, index])),
  };
}

const body = `/* Auto-generated by scripts/generate-agent-icon-sprites.mjs. Do not edit manually. */
export const AGENT_ICON_SPRITES = ${JSON.stringify(manifest, null, 2)} as const;

export type AgentIconSpriteMode = keyof typeof AGENT_ICON_SPRITES;
export type AgentIconSpriteId = keyof typeof AGENT_ICON_SPRITES.dark.cells;
`;

await fs.writeFile(manifestPath, body);
console.log(`Generated ${ids.length} agent icon sprite cells per theme`);
