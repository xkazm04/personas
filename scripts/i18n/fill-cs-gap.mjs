function setByPath(obj, path, value) {
  const parts = path.split('.');
  let node = obj;
  for (let i = 0; i < parts.length - 1; i++) {
    const key = parts[i];
    if (node[key] === undefined || typeof node[key] !== 'object' || Array.isArray(node[key])) {
      node[key] = {};
    }
    node = node[key];
  }
  node[parts[parts.length - 1]] = value;
}

const data = JSON.parse(readFileSync(CS_PATH, 'utf8'));
let added = 0;
for (const [key, value] of Object.entries(FILLS)) {
  setByPath(data, key, value);
  added++;
}
writeFileSync(CS_PATH, JSON.stringify(data, null, 2) + '\n');
console.log(`✓ cs — added ${added} keys`);
