#!/usr/bin/env node
/**
 * Extracts Tauri command names from src-tauri/src/lib.rs invoke_handler
 * and generates a TypeScript union type for type-safe invoke calls.
 *
 * Usage: node scripts/generate-command-names.mjs
 */
import { readFileSync, writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const LIB_RS = resolve(ROOT, "src-tauri/src/lib.rs");
const OUTPUT = resolve(ROOT, "src/lib/commandNames.generated.ts");

const libRs = readFileSync(LIB_RS, "utf-8");

// Extract the invoke_handler block
const handlerMatch = libRs.match(/invoke_handler\(tauri::generate_handler!\[\s*([\s\S]*?)\]\)/);
if (!handlerMatch) {
  console.error("Could not find invoke_handler block in lib.rs");
  process.exit(1);
}

const block = handlerMatch[1];
const commands = [];

for (const line of block.split("\n")) {
  const trimmed = line.trim();
  // Skip comments and cfg attributes
  if (trimmed.startsWith("//") || trimmed.startsWith("#[")) continue;
  if (!trimmed || trimmed === "])" || trimmed === "[") continue;

  // Extract the final segment (function name) from paths like commands::core::personas::list_personas,
  const match = trimmed.match(/(?:^|\s)([\w:]+?)(?:,?\s*$)/);
  if (match) {
    const fullPath = match[1];
    const name = fullPath.includes("::") ? fullPath.split("::").pop() : fullPath;
    if (name) commands.push(name);
  }
}

// Deduplicate (shouldn't happen, but be safe)
const unique = [...new Set(commands)].sort();

const output = `// AUTO-GENERATED by scripts/generate-command-names.mjs — DO NOT EDIT
// Re-run: node scripts/generate-command-names.mjs
//
// Generated from src-tauri/src/lib.rs invoke_handler (${unique.length} commands)

/**
 * Union of every Tauri command name registered in the invoke_handler.
 * Using this type in \`invokeWithTimeout\` ensures command name typos are
 * caught at compile time.
 */
export type CommandName =
${unique.map((c) => `  | "${c}"`).join("\n")};
`;

writeFileSync(OUTPUT, output, "utf-8");
console.log(`Generated ${OUTPUT} with ${unique.length} command names.`);
