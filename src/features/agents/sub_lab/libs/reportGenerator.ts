// ── Markdown generation ───────────────────────────────────────────

interface AggRow {
  label: string;
  compositeScore: number;
  avgToolAccuracy: number;
  avgOutputQuality: number;
  avgProtocolCompliance: number;
  totalCost: number;
  avgDuration: number;
}

function markdownSummary(title: string, rows: AggRow[], scenarios: string[]): string {
  let md = `# ${title}\n\n`;
  md += `| | Composite | Tool Acc. | Output Q. | Protocol | Cost | Avg Duration |\n`;
  md += `|---|:---:|:---:|:---:|:---:|---:|---:|\n`;
  for (const r of rows) {
    md += `| **${r.label}** | ${r.compositeScore} | ${r.avgToolAccuracy} | ${r.avgOutputQuality} | ${r.avgProtocolCompliance} | ${fmtCost(r.totalCost)} | ${fmtDuration(r.avgDuration)} |\n`;
  }
  md += `\n**Scenarios tested:** ${scenarios.length}\n`;
  return md;
}

// ── Public API ────────────────────────────────────────────────────

export type LabRunAny = LabArenaRun | LabAbRun | LabMatrixRun | LabEvalRun;
export type LabResultAny = LabArenaResult | LabAbResult | LabMatrixResult | LabEvalResult;
export type ReportMode = 'arena' | 'ab' | 'eval' | 'matrix';

export function generateHtmlReport(
  mode: ReportMode,
  run: LabRunAny,
  results: LabResultAny[],
  personaName: string,
): string {
  switch (mode) {
    case 'arena':
      return arenaHtml(run as LabArenaRun, results as LabArenaResult[], personaName);
    case 'ab':
      return abHtml(run as LabAbRun, results as LabAbResult[], personaName);
    case 'eval':
      return evalHtml(run as LabEvalRun, results as LabEvalResult[], personaName);
    case 'matrix':
      return matrixHtml(run as LabMatrixRun, results as LabMatrixResult[], personaName);
  }
}

export function generateMarkdownReport(
  mode: ReportMode,
  run: LabRunAny,
  results: LabResultAny[],
  personaName: string,
): string {
  switch (mode) {
    case 'arena': {
      const { aggregates, scenarios } = aggregateArenaResults(results as LabArenaResult[]);
      return markdownSummary(`Arena Report — ${personaName}`, aggregates.map((a) => ({ label: a.modelId, ...a })), scenarios);
    }
    case 'ab': {
      const { versionAggs, scenarios } = aggregateAbResults(results as LabAbResult[]);
      return markdownSummary(`A/B Report — ${personaName}`, versionAggs.map((a) => ({ label: `v${a.versionNumber}`, ...a })), scenarios);
    }
    case 'eval': {
      const { versionAggs } = buildEvalGridData(results as LabEvalResult[]);
      const scenarios = new Set((results as LabEvalResult[]).map((r) => r.scenarioName));
      return markdownSummary(`Eval Report — ${personaName}`, versionAggs.map((a) => ({ label: `v${a.versionNumber}`, ...a })), [...scenarios]);
    }
    case 'matrix': {
      const { variantAggs, scenarios } = aggregateMatrixResults(results as LabMatrixResult[]);
      const title = `Matrix Report — ${personaName}`;
      let md = markdownSummary(title, variantAggs.map((a) => ({ label: a.variant === 'current' ? 'Current' : 'Draft', ...a })), scenarios);
      if ((run as LabMatrixRun).draftChangeSummary) {
        md += `\n**Requested change:** ${(run as LabMatrixRun).draftChangeSummary}\n`;
      }
      return md;
    }
  }
}

export function downloadHtmlReport(html: string, filename: string): void {
  const blob = new Blob([html], { type: 'text/html;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
