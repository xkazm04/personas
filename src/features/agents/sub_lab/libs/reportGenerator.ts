function metricBar(label: string, value: number): string {
  return `<div class="metric-row"><span class="metric-label">${escHtml(label)}</span><span style="color:${scoreHexColor(value)};font-weight:600">${value}/100</span></div>
<div class="bar-bg"><div class="bar-fill" style="width:${Math.max(value, 2)}%;background:${scoreHexColor(value)}"></div></div>`;
}

// ── Arena report ──────────────────────────────────────────────────

function arenaHtml(run: LabArenaRun, results: LabArenaResult[], personaName: string): string {
  const { aggregates, bestModelId, scenarios } = aggregateArenaResults(results);
  const gridCls = aggregates.length <= 2 ? 'grid-2' : 'grid-3';

  let body = '<h2>Model Performance</h2>';
  body += `<div class="grid ${gridCls}">`;
  for (const agg of aggregates) {
    const isBest = agg.modelId === bestModelId;
    body += `<div class="card">
      <div style="display:flex;justify-content:space-between;align-items:center;margin-bottom:0.5rem">
        <strong>${escHtml(agg.modelId)}</strong>
        ${isBest ? '<span class="badge winner">Best</span>' : ''}
      </div>
      <div class="score" style="color:${scoreHexColor(agg.compositeScore)}">${agg.compositeScore}<span style="font-size:0.7rem;font-weight:400;color:var(--muted)">/100 ${scoreLabel(agg.compositeScore)}</span></div>
      ${metricBar('Tool Accuracy', agg.avgToolAccuracy)}
      ${metricBar('Output Quality', agg.avgOutputQuality)}
      ${metricBar('Protocol Compliance', agg.avgProtocolCompliance)}
      <div class="metric-row" style="margin-top:0.5rem"><span class="metric-label">Cost</span><span>${fmtCost(agg.totalCost)}</span></div>
      <div class="metric-row"><span class="metric-label">Avg Duration</span><span>${fmtDuration(agg.avgDuration)}</span></div>
    </div>`;
  }
  body += '</div>';

  body += scenarioTable(scenarios, aggregates.map((a) => a.modelId), results, 'modelId');

  return wrapHtml(`Arena Report — ${personaName}`, body, {
    date: fmtDate(run.createdAt),
    mode: 'Arena',
    persona: personaName,
  });
}

// ── A/B report ────────────────────────────────────────────────────

function abHtml(run: LabAbRun, results: LabAbResult[], personaName: string): string {
  const { versionAggs, scenarios, winnerId } = aggregateAbResults(results);

  let body = '<h2>Version Comparison</h2>';
  body += '<div class="grid grid-2">';
  for (const agg of versionAggs) {
    const isWinner = agg.versionId === winnerId;
    body += `<div class="card">
      <div style="display:flex;justify-content:space-between;align-items:center;margin-bottom:0.5rem">
        <strong>Version ${agg.versionNumber}</strong>
        ${isWinner ? '<span class="badge winner">Winner</span>' : ''}
      </div>
      <div class="score" style="color:${scoreHexColor(agg.compositeScore)}">${agg.compositeScore}<span style="font-size:0.7rem;font-weight:400;color:var(--muted)">/100 ${scoreLabel(agg.compositeScore)}</span></div>
      ${metricBar('Tool Accuracy', agg.avgToolAccuracy)}
      ${metricBar('Output Quality', agg.avgOutputQuality)}
      ${metricBar('Protocol Compliance', agg.avgProtocolCompliance)}
      <div class="metric-row" style="margin-top:0.5rem"><span class="metric-label">Cost</span><span>${fmtCost(agg.totalCost)}</span></div>
      <div class="metric-row"><span class="metric-label">Avg Duration</span><span>${fmtDuration(agg.avgDuration)}</span></div>
    </div>`;
  }
  body += '</div>';

  body += abScenarioTable(scenarios, versionAggs, results);

  return wrapHtml(`A/B Report — ${personaName}`, body, {
    date: fmtDate(run.createdAt),
    mode: 'A/B',
    persona: personaName,
  });
}

